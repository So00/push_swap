#include "push_swap.h"

int		ft_get_min(t_push_ar *a, int len)
{
	int		i;
	int		min;

	i = -1;
	while (++i < len)
		if (i == 0 || a->ar[i] < min)
			min = a->ar[i];
	return (min);
}
